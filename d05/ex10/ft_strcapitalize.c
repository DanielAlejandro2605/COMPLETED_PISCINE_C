int		iss_alpha(char c)
{
	if ((c >= 'a') && (c <= 'z'))
		return (1);
	if ((c >= 'A') && (c <= 'Z'))
		return (1);
	if ((c >= '0') && (c <= '9'))
		return (1);
	return (0);
}

int		is_maj(char c)
{
	if ((c >= 'A') && (c <= 'Z'))
		return (1);
	return (0);
}

char	is_min(char c)
{
	if ((c >= 'a') && (c <= 'z'))
		return (1);
	return (0);
}

char	*ft_strcapitalize(char *str)
{
	int	i;
	int	word_new;

	i = 0;
	word_new = 0;
	while (str[i])
	{
		if ((is_min(str[i])) && (word_new == 0))
			str[i] = str[i] - 32;
		else if ((is_maj(str[i])) && (word_new == 1))
			str[i] = str[i] + 32;
		if (!(iss_alpha(str[i])))
			word_new = 0;
		else
			word_new = 1;
		i++;
	}
	return (str);
}



