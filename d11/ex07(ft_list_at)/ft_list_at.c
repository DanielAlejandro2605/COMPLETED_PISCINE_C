#include "ft_list.h"

t_list	*ft_list_at(t_list *begin_list, unsigned int nbr)
{
	t_list			*list;
	unsigned int	i;

	if (nbr == 0)
		return ((void *)0);
	list = begin_list;
	i = 1;
	while (i < nbr)
	{
		if (!list)
			return ((void *)0);
		list = list->next;
		i++;
	}
	return (list);
}



