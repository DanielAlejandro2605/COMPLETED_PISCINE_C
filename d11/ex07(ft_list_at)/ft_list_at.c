/* ************************************************************************** */

#include "ft_list.h"

t_list		*ft_list_at(t_list *begin_list, unsigned int nbr)
{
	unsigned int	i;
	t_list			*list;

	if (nbr == 0)
		return ((void *)0);
	list = begin_list;
	i = 1;
	while (i < nbr)
	{
		if (list->next)
			list = list->next;
		else
			return ((void *)0);
		i++;
	}
	return (list);
}


