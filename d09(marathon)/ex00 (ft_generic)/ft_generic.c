#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	while (*str)
		ft_putchar(*str++);
}

void	ft_generic(void)
{
	ft_putstr("Tut tut ; Tut tut");
	ft_putchar('\n');
}

