int		ft_ultimate_range(int **range, int min, int max)
{
	int i;
	int *arr;

	if (min >= max)
	{
		range = NULL;
		return (0);
	}
	arr = (int*)malloc(sizeof(int) * (max - min));
	i = min;
	while (min < max)
	{
		arr[min - i] = min;
		min++;
	}
	*range = arr;
	return (max - i);
}


