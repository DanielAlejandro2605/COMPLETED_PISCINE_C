#include <stdlib.h>

/* ************************************************************************** */

int	*ft_map(int *tab, int length, int(*f)(int))
{
	int	*mytab;
	int	i;

	if ((mytab = (int*)malloc(sizeof(*tab) * length)) == ((void *)0))
		return ((void *)0);
	i = 0;
	while (i < length)
	{
		mytab[i] = f(tab[i]);
		i++;
	}
	return (mytab);
}


