/* Эта функция подсчитывает и возвращает количество символов в строке. */


int		ft_strlen(char *str)
{
	int n;						/* обьявляем пременную int. она будет использованная как счетчик для цикла */

	n = 0;						/* инициализируем переменную нулем так ка именно через нее мы будем обрашатся к нулевому элементу массива, потом первому элементу и т.д */
	while (str[n] != '\0')		/* с помощью переменной обращаемся к нулевому элементу */ /*если он не равен завершающему символоу массива '\0' 
								то увеличиваем число на один и проверяется сл. элемент массива */ /* если  он равен завершающему символоу массива '\0' то цикл прекращается 
								и программа идет к след нструкции которая у нас return (i);    */
	{
		n++;
	}
	return (n);					/* возвращает число которое указывает на количество элементов в массиве */
}


