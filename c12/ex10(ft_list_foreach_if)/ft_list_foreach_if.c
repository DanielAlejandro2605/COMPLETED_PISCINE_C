/* ************************************************************************** *
 *
 * 
 *	• Создайте функцию «ft_list_foreach_if», которая применяет функцию, задан-
 *	ную в качестве аргумента, к информации, содержащейся в некоторых ссылках 
 *	списка. Справочная информация, а также сравнительная функция должны позво-
 *	лить нам выбрать правильные ссылки в списке:
 *
 *									те, которые «равны» справочной информации.
 * 
 * 
 *	• Вот как эта функция должна быть объявлена :
 *
 *		void	ft_list_foreach_if(t_list *begin_list, void (*f)(void *), void *data_ref, int (*cmp)());
 * 
 * 
 * 
 * 
 *	• Функции, обозначенные f и cmp, будут использоваться следующим образом:
 * 
 * 											(*f)(list_ptr->data);
 *											(*cmp)(list_ptr->data, data_ref);
 * 
 * 
 * 
 *	• Например, функция 'cmp' может быть 'ft_strcmp' ...
 * 
 * 
 * 
 * ************************************************************************** */
/* ************************************************************************** */
/* ************************************************************************** */

#include "ft_list.h"

void	ft_list_foreach_if(t_list *begin_list, void (*f)(void *), void *data_ref, int (*cmp)())
{
	while (begin_list)
	{
		if ((*cmp)(begin_list->data, data_ref) == 0)
			(*f)(begin_list->data);
		begin_list = begin_list->next;
	}
}

