/* ************************************************************************** */
/* ************************************************************************** */


int		ft_putchar(char c)
{
	write(1, &c, 1);

	return (0);
}

void	ft_print_manager(int x, char startchar, char midchar, char endchar)
{
	ft_putchar(startchar);
	while ((x - 1) > 1)
	{
		ft_putchar(midchar);
		x--;
	}
	if (x > 1)
		ft_putchar(endchar);
	ft_putchar('\n');
}

void	rush(int x, int y)
{
	if (x > 0 && y > 0)
	{
		ft_print_manager(x, 'o', '-', 'o');
		y--;
		while (y > 1)
		{
			ft_print_manager(x, '|', ' ', '|');
			y--;
		}
		if (y > 0)
			ft_print_manager(x, 'o', '-', 'o');
	}
	return ;
}

int		main(void)
{
	rush(5, 3);
	
	return (0);
}

