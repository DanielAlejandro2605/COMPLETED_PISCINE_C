void	ft_putnbr(int nb)
{
	int	temp;
	int	size;

	size = 1;
	if (nb < 0)
	{
		ft_putchar('-');
		nb = -nb;
	}
	temp = nb;
	while ((temp /= 10) > 0)
		size *= 10;
	temp = nb;
	while (size)
	{
		ft_putchar((char)((temp / size)) + 48);
		temp %= size;
		size /= 10;
	}
}


void ft_putnbrtab(int *tab, int n)
{
	int i = 0;
	while(i < n)
	{
		ft_putnbr(tab[i]);
		ft_putchar('-');
		i++;
	}
}

