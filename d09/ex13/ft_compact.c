int		ft_compact(char **tab, int length)
{
	int		i;
	int		j;

	i = 0;
	while (i < length)
	{
		while (*(tab + i))
			i += 1;
		j = i;
		length -= 1;
		while (j < length)
		{
			*(tab + j) = *(tab + j + 1);
			j += 1;
		}
	}
	return (length);
}





